var alphas:string[]=[]; 

//Pushing One By One
alphas.push("Praveen");
alphas.push("Abhi");
alphas.push("Bharath");
console.log(alphas);
//['Praveen', 'Abhi', 'Bharath' ]

//pushing Group of Elements at a Time
alphas.push("sai", "Nagesh", "Sampath");
console.log(alphas);

//[ 'Praveen', 'Abhi', 'Bharath', 'sai', 'Nagesh', 'Sampath' ]


console.log(alphas.pop()); //removes Last element
console.log(alphas.pop()); //removes Last element



